package models

// Bank представляет информацию о банке/кредитном учреждении.
// Используется для получения информации о банке, в котором открыт счет.
//
// Информация о банке требуется при:
//   - Указании банковского счета в счете-фактуре
//   - Регистрации банковских счетов в системе
//   - Выполнении платежей
//   - Учете денежных потоков
//
// Содержит основные идентификационные данные банка, необходимые
// для валидации банковских счетов и проведения платежей.
type Bank struct {
	// ID - Уникальный идентификатор банка в системе ТУНДУК.
	// Используется для внутренней идентификации банка.
	// Пример: 1, 2, 3 и т.д.
	ID int64 `json:"id"`

	// Name - Полное наименование банка на русском языке.
	// Официальное название кредитного учреждения.
	// Примеры:
	//   - "Азиатский банк"
	//   - "Кыргызский государственный банк"
	//   - "Универсал Банк"
	//   - "Берёзка Бизнес Банк"
	Name string `json:"name"`

	// BIK - Банковский идентификационный код.
	// Уникальный код банка в системе платежей Кыргызской Республики.
	// Присваивается Национальным банком Кыргызской Республики.
	// Используется для маршрутизации платежей и идентификации банка.
	// Формат: обычно 5-6 цифр (например, "110014" для НБКР)
	BIK string `json:"bik"`

	// AddressText - Адрес главного офиса банка.
	// Полный почтовый адрес расположения банка или его основного офиса.
	// Используется для справочных целей и контактной информации.
	// Примеры:
	//   - "г. Бишкек, ул. Чуй, 123"
	//   - "720040, Кыргызская Республика, г. Бишкек, ул. Pravdy 96"
	AddressText string `json:"addressText"`
}
