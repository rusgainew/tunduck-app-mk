package models

// BankAccount представляет банковский счет юридического лица.
// Содержит полную информацию о счете, открытом в кредитном учреждении.
//
// Банковские счета используются для:
//   - Указания реквизитов для поступления платежей в счетах-фактурах
//   - Хранения информации о счетах различных контрагентов
//   - Выполнения платежей и переводов
//   - Финансового учета и отчетности
//
// Каждый счет связан с конкретным банком и валютой,
// и может быть отмечен как активный или неиспользуемый.
type BankAccount struct {
	// ========== ИДЕНТИФИКАТОРЫ ==========

	// ID - Уникальный идентификатор банковского счета в системе ТУНДУК.
	// Используется для ссылки на счет из других таблиц и документов.
	// Пример: 1, 2, 3 и т.д.
	ID int64 `json:"id"`

	// ========== ОСНОВНЫЕ РЕКВИЗИТЫ СЧЕТА ==========

	// AccountName - Название/описание банковского счета.
	// Условное название для идентификации счета внутри организации.
	// Может отличаться от номера счета и служить для удобства пользователя.
	// Примеры:
	//   - "Основной расчетный счет"
	//   - "Счет для зарплаты сотрудников"
	//   - "Счет для поступления от покупателей"
	//   - "Валютный счет в USD"
	AccountName string `json:"accountName"`

	// BankAccount - Номер банковского счета.
	// Полный номер счета, открытого в банке.
	// Уникально идентифицирует счет в банке.
	// Используется для отправки платежей и финансовых операций.
	// Формат: обычно 20-24 цифры (в зависимости от страны и банка)
	// Примеры: "0012000012345678901234", "KG0123 KGBK 0000 0001"
	BankAccount string `json:"bankAccount"`

	// ContractorTIN - ИНН (Персональный идентификационный номер) контрагента.
	// Идентификатор организации/физического лица, владельца этого счета.
	// Используется для связи счета с его владельцем.
	// Формат: 14 цифр (например, "12345678901234")
	// Если счет принадлежит текущей организации, это может быть ее ИНН.
	// Если счет партнера/контрагента, это его ИНН.
	ContractorTIN string `json:"contractorTin"`

	// ========== СТАТУСЫ И КЛАССИФИКАЦИЯ ==========

	// IsUsed - Флаг активности счета.
	// true: счет активен и используется в операциях
	// false: счет неактивен, архивирован или больше не используется
	// Позволяет хранить исторические данные, не удаляя информацию.
	IsUsed bool `json:"isUsed"`

	// IsResident - Флаг резидентства владельца счета.
	// true: счет принадлежит резиденту Кыргызской Республики
	// false: счет принадлежит нерезиденту (иностранное лицо/компания)
	// Влияет на применение налогов и таможенных процедур при платежах.
	IsResident bool `json:"isResident"`

	// ========== СПРАВОЧНЫЕ ДАННЫЕ ==========

	// Currency - Валюта, в которой открыт счет.
	// Определяет, в какой валюте хранятся деньги на счете и
	// в какой валюте должны проводиться платежи.
	// Примеры:
	//   - code="KGS", name="Кыргызский сом"
	//   - code="USD", name="Доллар США"
	//   - code="RUB", name="Российский рубль"
	//   - code="EUR", name="Евро"
	// Используется для проверки совместимости платежей и расчета курсов обмена.
	Currency *ReferenceItem `json:"currency"`

	// Bank - Информация о банке, в котором открыт счет.
	// Содержит название, БИК, адрес банка и другую информацию.
	// Необходима для маршрутизации платежей и верификации счета.
	// Может быть nil, если информация о банке недоступна.
	Bank *Bank `json:"bank"`
}
