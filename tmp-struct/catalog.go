package models

// Catalog представляет справочник товаров/услуг организации.
// Это внутренний справочник каждой организации, содержащий перечень
// товаров и услуг, которые она производит, продает или покупает.
//
// Использование каталога:
//   - Стандартизация описания товаров/услуг при создании счетов
//   - Быстрое заполнение счетов выбором из справочника
//   - Ведение истории товаров, которые продавались/покупались
//   - Управление классификацией и кодировкой товаров
//
// Каталог облегчает работу с документами и обеспечивает единообразие
// в описании товаров/услуг в различных счетах и договорах.
type Catalog struct {
	// ========== ИДЕНТИФИКАТОРЫ ==========

	// ID - Уникальный идентификатор записи в справочнике.
	// Используется для ссылки на этот товар/услугу из счетов и других документов.
	// Пример: 1, 2, 3 и т.д.
	ID int64 `json:"id"`

	// ========== ОСНОВНЫЕ СВЕДЕНИЯ О ТОВАРЕ/УСЛУГЕ ==========

	// Name - Полное наименование товара или оказываемой услуги.
	// Описание товара, как оно должно появляться в счетах-фактурах.
	// Должно быть четким, полным и недвусмысленным.
	// Примеры:
	//   - "Портативный компьютер ASUS VivoBook 15 (процессор Intel i7)"
	//   - "Услуга дизайна веб-сайта (от 40 до 80 часов работы)"
	//   - "Услуга консультирования по налоговому праву (1 час)"
	//   - "Кабель сетевой UTP категория 6, метр"
	Name string `json:"name"`

	// Number - Внутренний номер товара в справочнике организации.
	// Уникальный идентификатор товара в системе организации.
	// Может использоваться для быстрого поиска и идентификации.
	// Формат зависит от внутренней политики организации.
	// Примеры: "T001", "SKU-12345", "GOOD-2024-001"
	Number string `json:"number"`

	// ========== КЛАССИФИКАЦИОННЫЕ КОДЫ ==========

	// TNVEDCode - Код товара по ТНВЭД (Таможенная номенклатура внешнеэкономической деятельности).
	// Используется для таможенной классификации товара при импорте/экспорте.
	// Определяет применяемые таможенные пошлины, налоги и льготы.
	// Формат: 10 цифр (например, "8471.30.90")
	// Заполняется в основном для товаров, которые ввозятся/вывозятся.
	// Для услуг и отечественных товаров может быть пусто.
	TNVEDCode string `json:"tnvedCode"`

	// GKEDCode - Код вида деятельности по ГКЭД (Государственный классификатор видов экономической деятельности).
	// Определяет, к какому виду деятельности относится товар/услуга.
	// Используется для налоговой классификации и отраслевого анализа.
	// Может быть структурирован (например, "47.25.1" для торговли электроникой).
	// Для многих товаров может быть пусто.
	GKEDCode string `json:"gkedCode"`

	// ========== СПРАВОЧНЫЕ ДАННЫЕ (классификаторы) ==========

	// UnitClassification - Единица измерения товара/услуги.
	// Определяет, в каких единицах измеряется количество товара.
	// Важна для правильной интерпретации количества при создании счетов.
	// Примеры кодов и названий:
	//   - code="PCS", name="Штука"
	//   - code="KG", name="Килограмм"
	//   - code="M", name="Метр"
	//   - code="HOUR", name="Час"
	//   - code="L", name="Литр"
	//   - code="DAY", name="День"
	// Используется для корректного отображения и расчетов.
	UnitClassification *ReferenceItem `json:"unitClassification"`

	// CatalogType - Тип элемента в справочнике (товар или услуга).
	// Различает материальные товары от нематериальных услуг.
	// Влияет на применимость различных налогов и способ расчетов.
	// Примеры кодов и названий:
	//   - code="GOODS", name="Товар"
	//   - code="SERVICE", name="Услуга"
	//   - code="WORK", name="Работа"
	CatalogType *ReferenceItem `json:"catalogType"`
}
