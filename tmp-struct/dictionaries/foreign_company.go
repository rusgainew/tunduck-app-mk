package dictionaries

// ForeignCompany представляет иностранную компанию или иностранного контрагента.
// Используется для хранения информации об иностранных партнерах,
// с которыми организация осуществляет внешнеэкономическую деятельность.
//
// Отличие от обычного Party (юридического лица):
//   - Party используется для резидентов Кыргызской Республики с ИНН
//   - ForeignCompany используется для нерезидентов (иностранных компаний)
//
// Использование:
//   - Экспорт товаров иностранным покупателям
//   - Импорт товаров от иностранных поставщиков
//   - Оказание услуг иностранными компаниями
//   - Управление контрагентами из-за границы
//
// Содержит основные реквизиты иностранной компании, достаточные
// для идентификации и проведения трансграничных операций.
type ForeignCompany struct {
	// ========== ИДЕНТИФИКАТОРЫ ==========

	// ID - Уникальный идентификатор иностранной компании в системе ТУНДУК.
	// Используется для ссылки на компанию из счетов и других документов.
	// Пример: 1, 2, 3 и т.д.
	ID int64 `json:"id"`

	// ========== ОСНОВНЫЕ РЕКВИЗИТЫ КОМПАНИИ ==========

	// PIN - Идентификационный номер иностранной компании.
	// В разных странах это может быть:
	//   - Tax ID / TIN (Tax Identification Number) - Налоговый номер
	//   - VAT Number - Номер по НДС (для стран ЕС)
	//   - Registration Number - Номер регистрации
	//   - Business Number - Бизнес номер
	//   - UN/DUNS Number - Международный номер
	// Используется для идентификации компании в налоговой системе страны происхождения.
	// Может быть пусто, если компания не имеет налогового номера в своей стране.
	// Примеры: "DE123456789", "FR12345678901", "IT12345678901"
	PIN string `json:"pin"`

	// FullName - Полное наименование иностранной компании на языке оригинала.
	// Официальное зарегистрированное название компании.
	// Обычно указывается на английском языке или языке страны происхождения.
	// Примеры:
	//   - "Apple Inc."
	//   - "Microsoft Corporation"
	//   - "Samsung Electronics Co., Ltd."
	//   - "Siemens AG"
	// Если название очень длинное, может быть сокращено в пределах разумного.
	FullName string `json:"fullName"`

	// Дополнительные контактные и регистрационные данные
	// могут быть добавлены в зависимости от требований API и регулирующих органов
}
