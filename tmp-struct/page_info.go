package models

// PageInfo представляет информацию о пагинации (разбиение на страницы).
// Используется в API для работы с большими списками данных,
// позволяя получать информацию порциями вместо полного списка.
//
// Пагинация необходима для:
//   - Оптимизации производительности сети и сервера
//   - Улучшения времени отклика API
//   - Управления использованием памяти при больших объемах данных
//   - Удобного отображения данных в пользовательском интерфейсе
//
// Пример использования:
//   - Запрос: GET /api/query/invoice/realization/all-documents?page=0&size=20
//   - Ответ содержит первые 20 счетов и информацию о пагинации
//
// Клиент может использовать TotalPage для подсчета, сколько страниц
// нужно загрузить для получения всех данных, и Page для навигации.
type PageInfo struct {
	// Page - Номер текущей страницы (отсчет с нуля).
	// Определяет, какой набор результатов был получен.
	// Значение от 0 до (TotalPage - 1).
	// Пример: page=0 означает первую страницу, page=1 означает вторую страницу
	Page int `json:"page"`

	// Size - Количество элементов на одной странице.
	// Определяет, сколько записей вернется в этом ответе.
	// Обычно устанавливается клиентом при запросе API.
	// Пример: size=20 означает, что в ответе максимум 20 элементов
	Size int `json:"size"`

	// TotalElements - Общее количество элементов во всех страницах.
	// Полное количество записей, доступных в системе.
	// Используется для расчета общего числа страниц.
	// Пример: если TotalElements=100 и Size=20, то всего 5 страниц
	TotalElements int `json:"totalElements"`

	// TotalPage - Общее количество страниц.
	// Рассчитывается как: ceil(TotalElements / Size)
	// Позволяет клиенту знать, сколько всего страниц доступно.
	// Пример: если TotalElements=100 и Size=20, то TotalPage=5
	// Клиент может загружать страницы от 0 до (TotalPage-1)
	TotalPage int `json:"totalPage"`
}
