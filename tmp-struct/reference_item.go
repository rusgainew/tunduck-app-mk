package models

// ReferenceItem представляет справочный элемент с кодом и названием.
// Это универсальный тип, используемый для всех справочников в ESF API.
//
// Примеры использования:
//   - Валюта: code="KGS", name="Сом"
//   - Статус документа: code="DRAFT", name="Черновик"
//   - Тип оплаты: code="CASH", name="Наличная"
//   - Вид поставки: code="BY_TRANSPORT", name="Доставка транспортом"
//   - Классификация товаров: code="1234567890", name="Категория товаров"
//
// Поля:
//
//	Code: Уникальный код значения в справочнике (например, код валюты по ISO 4217)
//	Name: Человеческое название значения на русском языке
type ReferenceItem struct {
	// Code - Код справочного элемента.
	// Уникально идентифицирует значение в справочнике.
	// Примеры: "KGS" для сома, "RUB" для рубля, "DRAFT" для черновика
	Code string `json:"code"`

	// Name - Наименование справочного элемента.
	// Читаемое описание кода, используется для отображения пользователю.
	// Примеры: "Сом", "Рубль", "Черновик"
	Name string `json:"name"`
}
