package models

// VATTaxType представляет вид налога НДС (налог на добавленную стоимость)
// с информацией о ставке и названии налога.
//
// Используется для указания налоговой ставки в счетах-фактурах.
// В Кыргызстане могут применяться различные ставки НДС в зависимости
// от типа товаров, услуг и условий поставки.
//
// Примеры:
//   - Стандартная ставка: rate="12%", name="НДС 12%"
//   - Льготная ставка: rate="0%", name="НДС не применяется"
//   - Повышенная ставка: rate="15%", name="НДС 15%"
type VATTaxType struct {
	// Rate - Налоговая ставка НДС в процентах.
	// Строковое представление позволяет точно сохранить значение
	// без потери точности при вычислениях с целыми числами.
	// Примеры: "12%", "0%", "10%", "15%"
	Rate string `json:"rate"`

	// Name - Наименование налога.
	// Описание применяемой ставки НДС, используется для отображения
	// пользователю и документах.
	// Примеры: "НДС 12%", "НДС 0% (не применяется)", "НДС 15% (повышенная)"
	Name string `json:"name"`
}
